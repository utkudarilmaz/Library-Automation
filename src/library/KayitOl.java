    private void kaydolButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_kaydolButtonMouseClicked
        String query="INSERT INTO users VALUES(NULL,'"+isimField.getText()+"',NULL,NULL,NULL,'"+parolaField.getText()+"')";
        String query2="SELECT id FROM users where name='"+isimField.getText()+"'";
        Database database=new Database();
        try {
            database.runDatabase();
            database.state=database.con.createStatement();
            if(parolaField.getText().equals(parolaTekrarField.getText())){
                database.state.executeUpdate(query);
                database.state.close();
                
                database.state=database.con.createStatement();
                database.result=database.state.executeQuery(query2);
                database.result.next();
                JOptionPane.showMessageDialog(null,"Başarılı bir şekilde kayıt oldunuz!\nMÜŞTERİ NUMARANIZ:"+database.result.getString(1));
                JOptionPane.showMessageDialog(null,"\\nGiriş ekranına yönlendiriliyorsunuz!");
                setVisible(false);
                LoginPanel login = new LoginPanel();
                login.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"Girdiğiniz parolalar uyuşmuyor. Lütfen tekrar giriniz!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(KayitOl.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_kaydolButtonMouseClicked

